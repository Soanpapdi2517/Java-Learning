public class arithmeticop {
    public static void main(String[] args) {
        int x = 9;
        int y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);//int/int = int thats why where we should receive value in decimal it providing in integer
        System.out.println(x%y); // instead of getting quotient if we want to get remainder we can use modulus (%)
        //it is for int keyword only

        double a = 8;
        double b = 3;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
    }
}
